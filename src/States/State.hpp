#pragma once

// Это "интерфейс" или "контракт" для всех состояний нашего приложения.
// Любой экран, будь то главное меню, игра-кликер или калькулятор,
// должен будет унаследовать этот класс и реализовать его методы.
class State
{
public:
    // Виртуальный деструктор. Обязателен для базовых классов,
    // чтобы при удалении объекта через указатель на базовый класс
    // корректно вызывался деструктор дочернего класса.
    virtual ~State() = default;

    // Метод для обработки ввода (нажатия клавиш, клики мыши).
    virtual void HandleInput() = 0;

    // Метод для обновления логики состояния (движение, счетчики).
    // deltaTime - время, прошедшее с предыдущего кадра.
    // Позволяет делать логику независимой от FPS.
    virtual void Update(float deltaTime) = 0;

    // Метод для отрисовки всего, что есть в этом состоянии.
    virtual void Draw() = 0;
};